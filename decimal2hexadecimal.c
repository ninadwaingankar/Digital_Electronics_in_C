//decimal to hexadecimal program in C

#include<stdio.h>
#include<math.h>


int main()
{
	int decimal;
	printf("This program converts decimal number into binary & octal\n");
	printf("Enter a decimal number\n");
	scanf("%d",&decimal);
	printf("Equivalent hexadecimal number of %d is %X\n",decimal,decimal);
	return 0;
}


	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	